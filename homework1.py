print('Введите слово:')
a = input()
print('первая буква в слове: ' + a[0])
print('последняя буква в слове: ' + a[-1])
b = int(len(a) / 2)
if (len(a) - b) / 2 == int((len(a) - b) / 2):
    b = b-1
print('Вторая половина слова: ' + a[b:])
print('Слово наоборот: ' + a[::-1])
print('Каждый второй символ в слове: ' + a[1::2])